package Dominio;

public class Ventas {
	
	private int No_Ventas;
	private String Cliente;
	private float Costo_Total;
	private String Fecha;
	private String Producto_Manufacturado;
	
	public int getNo_Ventas() {
		return No_Ventas;
	}
	public void setNo_Ventas(int no_Ventas) {
		No_Ventas = no_Ventas;
	}
	public String getCliente() {
		return Cliente;
	}
	public void setCliente(String cliente) {
		Cliente = cliente;
	}
	public float getCosto_Total() {
		return Costo_Total;
	}
	public void setCosto_Total(int costo_Total) {
		Costo_Total = costo_Total;
	}
	public String getFecha() {
		return Fecha;
	}
	public void setFecha(String fecha) {
		Fecha = fecha;
	}
	public String getProducto_Manufacturado() {
		return Producto_Manufacturado;
	}
	public void setProducto_Manufacturado(String producto_Manufacturado) {
		Producto_Manufacturado = producto_Manufacturado;
	}
	
	
	

}
